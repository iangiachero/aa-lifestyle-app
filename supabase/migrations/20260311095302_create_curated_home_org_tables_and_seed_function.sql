DECLARE
  v_cat  RECORD;
  v_item RECORD;
BEGIN
  FOR v_cat IN
    SELECT c.id, c.icon_name, c.color_tag
    FROM curated_home_org_categories c
    ORDER BY c.sort_order
  LOOP
    FOR v_item IN
      SELECT i.title
      FROM curated_home_org_items i
      WHERE i.category_id = v_cat.id
      ORDER BY i.order_index
    LOOP
      INSERT INTO organization_tasks (user_id, icon, title, section, sub_tasks, completed, color_tag)
      VALUES (
        p_user_id,
        v_cat.icon_name,
        v_item.title,
        v_cat.id,
        '[]'::jsonb,
        false,
        v_cat.color_tag
      )
      ON CONFLICT (user_id, section, title) DO NOTHING;
    END LOOP;
  END LOOP;
END;
$$;

