/*
  # Create Curated Home Organization Tables, Seed Function, and Backfill

  ## Summary
  This migration sets up the full home organization curated data system, exactly
  mirroring the checklist pattern already in production.

  ## New Tables

  ### curated_home_org_categories
  - `id` (text, primary key) - Slug identifier (e.g., "daily-reset-adhd")
  - `name` (text) - Display name
  - `icon_name` (text) - Lucide React icon name
  - `color_tag` (text) - Hex color for the category
  - `sort_order` (int) - Display order (1-21)

  ### curated_home_org_items
  - `id` (uuid, primary key)
  - `category_id` (text, FK to curated_home_org_categories)
  - `title` (text) - Task text
  - `order_index` (int) - 0-based position within category

  ## New Functions
  - `seed_home_org_for_user(p_user_id UUID)` - Seeds all 840 individual task rows
    into organization_tasks for a given user. Idempotent via ON CONFLICT DO NOTHING.

  ## Security
  - Both curated tables have RLS enabled with a public authenticated SELECT policy
    so the seed function can read them
  - Seed function runs as SECURITY DEFINER

  ## Notes
  1. All 21 categories and 840 items (40 per category) are inserted in this migration
  2. A unique constraint is added to organization_tasks(user_id, section, title)
     to support idempotent seeding
  3. All existing users are backfilled immediately
*/

-- ────────────────────────────────────────────────────────────────────────────
-- 1. Curated categories table
-- ────────────────────────────────────────────────────────────────────────────
CREATE TABLE IF NOT EXISTS curated_home_org_categories (
  id         text PRIMARY KEY,
  name       text NOT NULL,
  icon_name  text NOT NULL DEFAULT 'Home',
  color_tag  text NOT NULL DEFAULT '#C9A962',
  sort_order int  NOT NULL DEFAULT 99
);

ALTER TABLE curated_home_org_categories ENABLE ROW LEVEL SECURITY;

DO $$
BEGIN
  IF NOT EXISTS (
    SELECT 1 FROM pg_policies
    WHERE tablename = 'curated_home_org_categories'
      AND policyname = 'Anyone authenticated can read curated home org categories'
  ) THEN
    CREATE POLICY "Anyone authenticated can read curated home org categories"
      ON curated_home_org_categories FOR SELECT
      TO authenticated
      USING (true);
  END IF;
END $$;

-- ────────────────────────────────────────────────────────────────────────────
-- 2. Curated items table
-- ────────────────────────────────────────────────────────────────────────────
CREATE TABLE IF NOT EXISTS curated_home_org_items (
  id          uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  category_id text NOT NULL REFERENCES curated_home_org_categories(id) ON DELETE CASCADE,
  title       text NOT NULL,
  order_index int  NOT NULL DEFAULT 0
);

CREATE INDEX IF NOT EXISTS curated_home_org_items_category_idx ON curated_home_org_items(category_id);

ALTER TABLE curated_home_org_items ENABLE ROW LEVEL SECURITY;

DO $$
BEGIN
  IF NOT EXISTS (
    SELECT 1 FROM pg_policies
    WHERE tablename = 'curated_home_org_items'
      AND policyname = 'Anyone authenticated can read curated home org items'
  ) THEN
    CREATE POLICY "Anyone authenticated can read curated home org items"
      ON curated_home_org_items FOR SELECT
      TO authenticated
      USING (true);
  END IF;
END $$;

